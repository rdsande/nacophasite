    protected function render()
    {
        $settings = $this->get_settings_for_display();
        if (empty($settings['slides'])) {
            return;
        }
        ?>

        <?php if ($settings['design_style'] === 'style_2'): ?>
    <?php else: ?>

        <section class="tp-project-area position-relative">
            <div class="tp-project-shape"></div>
            <div class="tp-project-wrapper">
                <div class="tp-project-active swiper-container">
                    <div class="swiper-wrapper">
                        <?php foreach ($settings['slides'] as $slide) :
                            if (!empty($slide['image']['id'])) {
                                $image = wp_get_attachment_image_url($slide['image']['id'], $settings['thumbnail_size']);
                            }
                        ?>
                        <div class="tp-project z-index swiper-slide mb-30">
                            <?php if (!empty($image)) : ?>
                            <div class="tp-project-img">
                                <img src="<?php print esc_url($image); ?>" class="img-fluid" alt="img not found">
                            </div>
                            <?php endif; ?>
                            <div class="tp-project-text">
                                <div class="tp-project-text-content">
                                    <?php if (!empty($slide['sub_title'])) : ?>
                                    <span class="tp-project-subtitle"><?php echo bdevs_element_kses_basic($slide['sub_title']); ?></span>
                                    <?php endif; ?>
                                    <?php if (!empty($slide['title'])) : ?>
                                    <h4 class="tp-project-title"><a href="<?php echo esc_url($slide['slide_url']); ?>"><?php echo bdevs_element_kses_basic($slide['title']); ?></a></h4>
                                    <?php endif; ?>
                                </div>
                                <?php if (!empty($image)) : ?>
                                <div class="tp-project-text-icon">
                                    <a class="popup-image" href="<?php print esc_url($image); ?>"><i class="fal fa-plus"></i></a>
                                </div>
                                <?php endif; ?>
                            </div>
                        </div>
                        <?php endforeach; ?>
                </div>
            </div>
        </section>

    <?php endif; ?>

        <?php
    }
