    /**
     * Render widget output on the frontend.
     *
     * Used to generate the final HTML displayed on the frontend.
     *
     * Note that if skin is selected, it will be rendered by the skin itself,
     * not the widget.
     *
     * @since 1.0.0
     * @access public
     */
    protected function render() {
        $settings = $this->get_settings_for_display();

        $this->add_inline_editing_attributes( 'description', 'none' );
        $this->add_render_attribute( 'description', 'class', 'content-desc' );

        ?>

        <?php if ( $settings['design_style'] === 'style_2' ): ?>

        <div class="row">
            <?php 
                foreach ( $settings['slides'] as $key => $slide ):
                    if (!empty($slide['image']['url'])) {
                        $image = wp_get_attachment_image_url( $slide['image']['id'], $settings['thumbnail_size'] );
                        if ( ! $image ) {
                            $image = $slide['image']['url'];
                        }
                    }
            ?> 
            <div class="col-xl-4 col-md-6">
                <div class="tp-service-details-feature text-center bg-gray-light mb-40">
                    <div class="tp-feature-three-icon mb-25">
                        <img src="<?php print get_template_directory_uri(); ?>/assets/img/icon/service-feature.png" class="img-fluid" alt="img not found">
                        <?php if( !empty($slide['selected_icon']) ): ?>
                            <?php bdevs_element_render_icon( $slide, 'icon', 'selected_icon', ['class' => 'bdevs-btn-icon'] ); ?>
                            <?php else: ?>
                                <img src="<?php echo esc_url($image); ?>" alt="icon" />
                        <?php endif; ?>
                    </div>

                    <?php if( $slide['title'] ): ?>
                    <h4 class="tp-service-details-feature-title"><a href="<?php echo esc_url( $slide['f_url'] ); ?>"><?php echo bdevs_element_kses_basic( $slide['title'] ); ?></a></h4>
                    <?php endif; ?>

                    <?php if( $slide['description'] ): ?>
                    <p><?php echo bdevs_element_kses_intermediate( $slide['description'] ); ?></p>
                    <?php endif; ?>
                </div>
            </div>
            <?php endforeach; ?>
        </div>

        <?php else: ?>

        <section class="tp-feature-area-three">
            <div class="container">
                <div class="row">
                    <?php foreach ( $settings['slides'] as $slide ):
                        if ( !empty($slide['image']['url']) ) {
                            $image = wp_get_attachment_image_url( $slide['image']['id'], $settings['thumbnail_size'] );
                            if ( ! $image ) {
                                $image = $slide['image']['url'];
                            }
                        }
                    ?>
                    <div class="col-xl-3 col-sm-6">
                        <div class="tp-feature-three text-centers mb-30">
                            <div class="tp-feature-three-icon mb-30">
                                <img src="<?php print get_template_directory_uri(); ?>/assets/img/icon/feature-bg.png" class="img-fluid" alt="img not found">
                                <?php if( !empty($slide['selected_icon']) ): ?>
                                    <?php bdevs_element_render_icon( $slide, 'icon', 'selected_icon', ['class' => 'bdevs-btn-icon'] ); ?>
                                    <?php else: ?>
                                        <img src="<?php echo esc_url($image); ?>" alt="icon" />
                                <?php endif; ?>
                            </div>
                            <div class="tp-feature-three-text">
                                <?php if( $slide['title'] ) : ?>
                                <h4 class="tp-feature-three-title mb-20"><a href="<?php echo esc_url( $slide['f_url'] ); ?>"><?php echo bdevs_element_kses_basic( $slide['title'] ); ?></a></h4>
                                <?php endif; ?>

                                <?php if( $slide['description'] ) : ?>
                                <p><?php echo bdevs_element_kses_basic( $slide['description'] ); ?></p>
                                <?php endif; ?>
                            </div>
                        </div>
                    </div>
                    <?php endforeach; ?>
                </div>
            </div>
        </section>

        <?php endif; ?>
        
        <?php
    }

