    protected function render()
    {

        $settings = $this->get_settings_for_display();
    ?>

    <?php if ($settings['design_style'] === 'style_2') : ?>

        <?php if ( !empty($settings['button_text']) ) : ?>
        <div class="tp-project-title-btn">
            <a href="<?php echo esc_url($settings['button_link']); ?>" class="yellow-btn"><i class="flaticon-enter"></i> <?php echo esc_html($settings['button_text']);?></a>
        </div>
        <?php endif; ?>

    <?php elseif ($settings['design_style'] === 'style_3'): ?>
        <?php if ($settings['title']) : ?>
            <div class="container">
                <div class="row align-items-center mb-55">
                    <div class="col-xl-6 col-lg-8 col-md-8 col-sm-8">
                        <div class="section__title section__title-3 mb-30 wow fadeInUp2" data-wow-delay=".2s">
                            <?php if ($settings['sub_title']) : ?>
                                <span class="sub-title bdevs-el-sub"><?php echo bdevs_element_kses_intermediate($settings['sub_title']); ?></span>
                            <?php endif; ?>
                            <?php printf('<%1$s %2$s>%3$s</%1$s>',
                                tag_escape($settings['title_tag']),
                                $this->get_render_attribute_string('title'),
                                $title
                            ); ?>
                            <?php if ($settings['description']) : ?>
                                <p <?php $this->print_render_attribute_string('details'); ?>><?php echo bdevs_element_kses_intermediate($settings['description']); ?></p>
                            <?php endif; ?>
                        </div>
                    </div>
                    <?php if ($settings['button_text']) : ?>
                    <div class="col-xl-6 col-lg-4 col-md-4 col-sm-4">
                        <div class="team__more d-flex justify-content-sm-end mb-85">
                            <?php if ($settings['button_text'] && ((empty($settings['button_selected_icon']) || empty($settings['button_selected_icon']['value'])) && empty($settings['button_icon']))) :
                                printf('<a %1$s href="%3$s">%2$s</a>',
                                    $this->get_render_attribute_string('button'),
                                    esc_html($settings['button_text']),
                                    esc_url($settings['button_link']['url'])
                                );
                            elseif (empty($settings['button_text']) && ((!empty($settings['button_selected_icon']) || empty($settings['button_selected_icon']['value'])) || !empty($settings['button_icon']))) :
                                ?>
                                <a <?php $this->print_render_attribute_string('button'); ?>><?php bdevs_element_render_icon($settings, 'button_icon', 'button_selected_icon'); ?></a>
                            <?php elseif ($settings['button_text'] && ((!empty($settings['button_selected_icon']) || empty($settings['button_selected_icon']['value'])) || !empty($settings['button_icon']))) :
                                if ($settings['button_icon_position'] === 'before'): ?>
                                    <a <?php $this->print_render_attribute_string('button'); ?>><span><?php bdevs_element_render_icon($settings, 'button_icon', 'button_selected_icon', ['class' => 'bdevs-btn-icon']); ?></span> <?php echo esc_html($settings['button_text']); ?>
                                    </a>
                                <?php
                                else: ?>
                                    <a <?php $this->print_render_attribute_string('button'); ?>><?php echo esc_html($settings['button_text']); ?>
                                        <span><?php bdevs_element_render_icon($settings, 'button_icon', 'button_selected_icon', ['class' => 'bdevs-btn-icon']); ?></span></a>
                                <?php
                                endif;
                            endif; ?>
                        </div>
                    </div>
                    <?php endif; ?>
                </div>
            </div>
        <?php endif; ?>

    <?php else: ?>

        <?php if ( !empty($settings['button_text']) ) : ?>
        <div class="tp-quality-btn">
            <a href="<?php echo esc_url($settings['button_link']); ?>" class="theme-btn radius-50"><?php echo esc_html($settings['button_text']);?></a>
        </div>
        <?php endif; ?>

    <?php endif; ?>

        <?php
    }
